</p>
<p>
    <i>
        Primeiro ponto importante é que áudio é uma área bem abrangente que embarca várias habilidades/gostos. 
        Existe área de composição, sound design, voiceover (locução), localização e por aí vai.. e que todas tem suas 
        especificidades.
    </i>
</p>
<p>
        Ele também menciona o 
        <?php href("Reaper", "https://www.reaper.fm/purchase.php"); ?> 
        , que pode ser testado de graça por um tempo e a licensa é bem em conta. Sendo 60 dólares para usar 
        profissionalmente se o lucro adquirido for menor que 20 mil dólares anual. O software é absurdamente poderoso 
        fazendo valer a pena.
</p>
<p>
    <i>
        áudio é um mundo bem vasto e com muitas possibilidades criativas [...], gravação, edição, mixagem e bibliotecas 
        de áudio. Sobre a parte de gravação vs bibliotecas de áudio (quais usar e quando usar), tem situações em que um 
        ou outro encaixa melhor, e muitas vezes combinamos os dois pra criar o som ideal.
    </i>
</p>
<p>
    <i>
        E tem toda a parte de implementação/tech também! Isso é mais avançado, mas é bem importante no mercado atual. 
        Manjar de middleware como 
        <?php href("FMOD", "https://www.fmod.com/learn"); ?> 
        ou 
        <?php href("WWISE", "https://www.audiokinetic.com/en/wwise/overview/"); ?>
        , e a parte de design e lógica de áudio ingame.
    </i>
</p>
<p>
    <i>
        Pra composição, há uma bela gama de softwares (DAWs) que a galera utiliza. Os principais são 
        <?php href("Logic Pro X (pra Mac)", "https://www.apple.com/br/logic-pro/"); ?>, 
        <?php href("Ableton Live", "https://www.ableton.com/en/live/what-is-live/"); ?>, 
        <?php href("FL Studio", "https://www.image-line.com/fl-studio/"); ?>, 
        <?php href("Reaper", "https://www.reaper.fm/purchase.php"); ?>, 
        <?php href("Studio One", "https://intl.presonus.com/pages/studio-one-pro"); ?>, 
        <?php href("Cubase", "https://www.steinberg.net/cubase/"); ?>, 
        <?php href("Pro Tools", "https://www.avid.com/pro-tools"); ?> 
        e por aí vai. Teoria musical não é um pré-requisito pra compor, mas ajuda MUUUUITO e facilita demais o 
        processo. 
    </i>
</p>
<p>
    <i>
        Acho que o mais importante aqui é vivenciar a música: ouvir muita coisa diferente, abrir a cabeça em 
        relação a diferentes estilos, criar uma larga base de referência e botar a mão na massa sem medo. 
        Gamejams são um belo ponto de partida pra galera se integrar, conhecer novas pessoas e testar suas habilidades 
        em projetos reais. Cada jogo tem uma necessidade totalmente diferente, o compositor vai ter que ser criativo de 
        modo a saber que instrumentação/mood/arranjo e mix usará em cada gênero pra cada situação. 
    </i>
</p>
<p>
    <i>
        Um jogo de terror ou suspense vai precisar de uma ambientação/música X, enquanto um jogo de ação com gráficos 
        bem coloridos e temática infantil vai precisar de outra paleta de sons totalmente diferente. O desafio do 
        compositor é se adaptar a cada mundo e entregar o melhor pra cada projeto.
    </i>
</p>
<p>
    <i>
        Existem compositores que se especializam num gênero específico e não fazem outros estilos, mas eu 
        particularmente acho que isso limita as possibilidades de achar trabalho, porque cada projeto tem uma demanda 
        completamente diferente e quanto mais gêneros o compositor domina e entende, mais chance de pegar um projeto 
        novo ele tem.
    </i>
</p>
<p>
    <i>
        Outras informações importantes no geral:
    </i>
</p>
<p>
    <i>
        <li>
            Sim, tem MUITA coisa pra aprender em áudio e é um mundo vasto e infinito, mas não se afobe. Vá aos poucos, sem medo. 
            Componha, participe de jams, mostre seu trabalho, se integre com outras pessoas, peça feedback de 
            outros artistas, outros músicos. Crie laços e as coisas vão se desenrolando 
            <br />
            <br />
        </li>
        <li>
            Vai ter um momento que você vai querer testar todos os plugins e VSTs do mundo. Normal. No final das 
            contas, mais importante pra parte de edição e mixagem: Aprenda profundamente o básico de cada processo 
            (equalização, compressão, reverberação, saturação, imagem stereo, fase/polaridade, síntese, etc... sim 
            é bastante coisa). Saber como esses elementos funcionam e conseguir mexer na mão, dominando, é bem mais 
            útil do que caçar presets nos plugins e ver "o que soa melhor" sem saber o que está acontecendo no 
            final das contas.
            <br />
            <br />
        </li>
        <li>
            Há muuuuuuuuuito material disponível online para estudo nos mais variados sites. Nunca tivemos tanto 
            acesso à informação. Estude! Veja vídeos sobre o assunto, mas não esqueça de botar a mão na massa, 
            fazer e aplicar o que assistiu :). Só memorizamos na prática. Tentativa e erro, erro, erro, erro até 
            chegar ao acerto. Cada vez melhor.
        </li>
    </i>
</p>
